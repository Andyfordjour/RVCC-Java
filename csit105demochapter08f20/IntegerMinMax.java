package csit105demochapter08f20;

/**
 * This program demonstrates MIN_VALUE and MAX_VALUE for Integer
 *
 * @author Tony Gaddis (et al)
 */

public class IntegerMinMax {

    /**
     * The main method is the program's starting point.
     *
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        System.out.printf("The minimum value for an "
                + "int is %,d\n", Integer.MIN_VALUE);
        System.out.printf("The maximum value for an "
                + "int is %,d\n", Integer.MAX_VALUE);

    }

}
